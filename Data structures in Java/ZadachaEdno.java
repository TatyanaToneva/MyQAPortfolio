//Write program to print sum of a given array elements.

public class ZadachaEdno {

	public static void main(String[] args) {
	double [] arrayPrintSum =	{2,3.5,14,87,5.2,-5,0};
	double sum = 0;
	for (int i=0; i < arrayPrintSum.length; i++) {
	  sum = sum + arrayPrintSum [i];
	  
	}
	System.out.println(sum);
		
	}
	
	

}

